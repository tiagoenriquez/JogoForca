    /**
     * Aciona os métodos decorrentes da submissão de uma letra ao sistema.
     *
     * @param evt
     */
    private void submeterLetra(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submeterLetra
        try {
            if (jogoEmAndamento) {
                jogo = jogoService.conferirLetra(textFieldLetra.getText().charAt(0), jogo);
                atualizarCampos();
                if (!jogoEmAndamento) {
                    buttonSubmeterLetra.setText("Outra partida");
                    atualizarCampos();
                    mostrarResultados();
                    labelNomePalavraSecreta.setText(palavra);
                    mostrarPercentagens();
                }
            } else {
                iniciarJogo(new PalavraService().sortear());
                buttonSubmeterLetra.setText("Submeter letra");
            }
            textFieldLetra.setText("");
        } catch (Exception exception) {
            Mensagem.erro(exception.getMessage());
        }
    }//GEN-LAST:event_submeterLetra

    /**
     * Encaminha para tela de configuração no modo correção de palavra.
     *
     * @param evt
     */
    private void corrigirPalavra(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_corrigirPalavra
        try {
            new TelaConfiguracao(null, new PalavraService().procurar(palavra)).setVisible(true);
        } catch (Exception exception) {
            Mensagem.erro(exception.getMessage());
        }
    }//GEN-LAST:event_corrigirPalavra

