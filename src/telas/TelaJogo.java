    /**
     * Inicia um jogo.
     *
     * @param palavra
     */
    private void iniciarJogo(String palavra) {
        jPanel1.setBackground(new Color(255, 191, 128));
        this.palavra = palavra;
        System.out.println(palavra);
        jogo = new Jogo(palavra);
        this.jogoService = new JogoService();
        jogoEmAndamento = true;
        atualizarCampos();
    }

    /**
     * Atualiza os dados na tela.
     */
    private void atualizarCampos() {
        labelNumeroAcertos.setText(String.valueOf(jogo.getAcertos()));
        labelNumeroChances.setText(String.valueOf(jogo.getChances()));
        labelNomePalavraSecreta.setText(String.valueOf(jogo.getPalavraSecreta()));
        jogoEmAndamento = jogo.getAcertos() < palavra.length() && jogo.getChances() > 0;
        labelForca.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagens/Erro " + String.valueOf(6 - jogo.getChances()) + ".JPG")));
        mostrarLabels();
    }

    /**
     * Mostra os resultados a depender de se o jogo está em andamento.
     */
    private void mostrarLabels() {
        labelResultado.setVisible(!jogoEmAndamento);
        labelPercentagemAcertos.setVisible(!jogoEmAndamento);
        labelPercentagemVitorias.setVisible(!jogoEmAndamento);
        labelNumeroPercentagemAcertos.setVisible(!jogoEmAndamento);
        labelNumeroPercentagemVitorias.setVisible(!jogoEmAndamento);
        buttonCorrigirPalavra.setVisible(!jogoEmAndamento);
    }

    /**
     * Escreve o resultado na tela.
     */
    private void mostrarResultados() {
        if (jogo.getAcertos() == palavra.length()) {
            labelResultado.setText("Parabéns! Você acertou!");
            jPanel1.setBackground(new Color(128, 191, 128));
            jogo.setVitoria(true);
        } else {
            labelResultado.setText("Que pena! Você errou!");
            jPanel1.setBackground(new Color(255, 128, 128));
        }
    }

    /**
     * Mostra as percentagens de acertos e vitórias ao longo das partidas.
     */
    private void mostrarPercentagens() {
        Object[] percentagens = jogoService.obterPercentagens(jogos, jogo);
        System.out.println("A percentagem de acertos é " + String.valueOf(percentagens[0]));
        labelNumeroPercentagemAcertos.setText(String.valueOf(percentagens[0]));
        labelNumeroPercentagemVitorias.setText(String.valueOf(percentagens[1]));
    }

    /**
     * Aciona o método que insere o ícone na barra de título.
     *
     * @throws Exception
     */
    private void inserirIcone() {
        try {
            new Global().logo(this);
        } catch (Exception exception) {
            Mensagem.erro(exception.getMessage());
        }
    }

