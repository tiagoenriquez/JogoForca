    /**
     * Aciona o serviço de inserção de palavra no banco de dados com o nome
     * escrito no campo nome.
     *
     * @param evt
     */
    private void inserir(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserir
        try {
            palavraService.inserir(new Palavra(textFieldNome.getText()));
        } catch (Exception exception) {
            Mensagem.erro(exception.getMessage());
        }
    }//GEN-LAST:event_inserir

    /**
     * Aciona o serviço de correção de palavra no banco de dados.
     *
     * @param evt
     */
    private void corrigir(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_corrigir
        try {
            palavraService.corrigir(new Palavra(Integer.parseInt(textFieldId.getText()), textFieldNome.getText()));
            Mensagem.sucesso("Palavra corrigida com sucedida");
        } catch (Exception exception) {
            Mensagem.erro(exception.getMessage());
        }
    }//GEN-LAST:event_corrigir

