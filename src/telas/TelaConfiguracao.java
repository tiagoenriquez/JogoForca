    /**
     * Desabilita o botão Corrigir se não houver palavra e desabilita o botão
     * Inserir se houver palavra.
     *
     * @param palavra
     */
    private void desabilitarBotaoDesnecessario(Palavra palavra) {
        if (palavra == null) {
            buttonCorrigir.setEnabled(false);
        } else {
            buttonInserir.setEnabled(false);
            textFieldId.setText(String.valueOf(palavra.getId()));
        }
    }

    /**
     * Aciona o método que insere o ícone na barra de título.
     *
     * @throws Exception
     */
    private void inserirIcone() {
        try {
            new Global().logo(this);
        } catch (Exception exception) {
            Mensagem.erro(exception.getMessage());
        }
    }

